static inline void terminal_log_handler(char level, const char *file, int line, const char *function, const char *log)
{
    struct timeval log_tv = {};
    gettimeofday(&log_tv, NULL);
    time_t log_now = (time_t)log_tv.tv_sec;
    struct tm log_timeinfo = {};
    localtime_r(&log_now, &log_timeinfo);
    char log_dateTime[128 * 2] = {};
    char log_dateStr[128] = {};
    strftime(log_dateStr, sizeof(log_dateStr), "%Y-%m-%d %H:%M:%S", &log_timeinfo);
    snprintf(log_dateTime, sizeof(log_dateTime), "%s.%06ld", log_dateStr, log_tv.tv_usec);
    fprintf(stdout, "[%s %c] [%s:%d %s] %s\n", log_dateTime, level, file, line, function, log);
}

static inline void log_print(char level, const char *format, const char *file, int line, const char *function, log_print_cb print_cb, ...)
{
    if (UTIL_LOG_LEVEL_TO_NUM(level) > UTIL_LOG_LEVEL_DEFAULT)
    {
        return;
    }

    char log_maxbuf[4096 + 4] = {};
    va_list args = {};

#pragma GCC diagnostic push
#pragma GCC diagnostic ignored "-Wvarargs"
    va_start(args, format);
#pragma GCC diagnostic ignored "-Wformat-nonliteral"
    vsnprintf(log_maxbuf, sizeof(log_maxbuf), format, args);
#pragma GCC diagnostic pop
    va_end(args);

    size_t log_size = strlen(log_maxbuf);
    if (log_size > 0)
    {
        size_t end = log_size - 1;
        while (end > 0 && (log_maxbuf[end] == '\n' || log_maxbuf[end] == '\r'))
        {
            end--;
        }
        log_maxbuf[(0 == end) ? end : end + 1] = '\0';
    }

    print_cb(level, file, line, function, log_maxbuf);
}

static inline void log_print_buf(const char *name, const void *buf, size_t length)
{
    size_t _len = (length);
    const uint8_t *_tmp_buff = (const uint8_t *)(buf);
    const uint8_t _BYTES_PER_COL = 32;
    char _row_buff[32 * 3 + 1] = {0};
    size_t _start_byte = 0;
    size_t _row_cnt = (_len) / _BYTES_PER_COL;
    size_t _last_bytes = (_len) % _BYTES_PER_COL;
    size_t _row_bytes = 0;

    if (_last_bytes)
        _row_cnt++;
    LOG_PRINT_DEBUG("Buffer[%s] start: total{bytes[%zu],rows[%zu]}", name, _len, _row_cnt);

    for (size_t __i = 0; __i < _row_cnt; ++__i)
    {
        memset(_row_buff, 0, _BYTES_PER_COL * 3);
        _start_byte = __i * _BYTES_PER_COL;
        _row_bytes = (_last_bytes && (__i == (_row_cnt - 1))) ? _last_bytes : _BYTES_PER_COL;

        for (size_t __j = 0; __j < _row_bytes; ++__j)
        {
            snprintf(&_row_buff[__j * 3], 4, "%02X ", _tmp_buff[_start_byte + __j]);
        }

        LOG_PRINT_DEBUG("[%03zu] %s", __i, _row_buff);
    }

    LOG_PRINT_DEBUG("Buffer[%s] end.", name);
}
